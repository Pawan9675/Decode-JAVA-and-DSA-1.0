package Week_1_Basics;

public class ModulusOperator {
    public static void main(String[] args) {
        // Note:
        // a % b = a [a<b]
        // a % -b = a % b
        // -a % b = -(a % b)
        // -a % -b = -(a % b)

        System.out.println(3%4);
        System.out.println(3%-4);
        System.out.println(-3%4);
    }
}
