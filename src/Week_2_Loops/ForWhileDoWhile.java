package Week_2_Loops;

public class ForWhileDoWhile {
    public static void main(String[] args) {
        //for loop
        for (int i = 11; i <= 10 ; i++) {
            System.out.println(i);
        }

        //while loop
        int i = 11;
        while (i<=10) {
            System.out.println(i);
            i++;
        }

        //do-while loop
        int j = 11;
        do {
            System.out.println(j);
            j++;
        }while (j<=10);


    }
}
